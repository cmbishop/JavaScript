function isAVowel(str) {
    switch (str) {
        case "a":
            return true;
            break;
        case "e":
            return true;
            break;
        case "i":
            return true;
            break;
        case "o":
            return true;
            break;
        case "u":
            return true;
            break;
        default:
            return false;
    }
}
